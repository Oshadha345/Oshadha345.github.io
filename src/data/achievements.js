export const getLatestAchievements = () => {
  return [...achievements].sort((a, b) => new Date(b.date) - new Date(a.date))
}

export const getAchievementsByType = (type) => {
  if (type === 'all') return getLatestAchievements()
  return achievements.filter(a => a.type === type).sort((a, b) => new Date(b.date) - new Date(a.date))
}

export const getAchievementsByYear = (year) => {
  return achievements.filter(a => new Date(a.date).getFullYear() === year)
}
